    private void FillLabels()
    {
        ItemLabel2.setText(this.nombreItem);
        PrecioLabel.setText(this.precio);
        
        try{
            String sql = "Select * from \"subastas\" s where s.id = ?";
            pst = conn.prepareStatement(sql);
            pst.setInt(1, this.idSubasta);
            rs = pst.executeQuery();
            
            while(rs.next())
            {            
                FechaLabel.setText(rs.getString(4));
                TiempoLabel.setText(rs.getString(6));
                DetallesLabel.setText(rs.getString(7));
                PrecioFinalLabel.setText(rs.getString(10));
            }
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
   
    
